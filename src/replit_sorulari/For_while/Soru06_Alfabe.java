package replit_sorulari.For_while;

public class Soru06_Alfabe {
    public static void main(String[] args) {
        // Print Alphabet on the console.
        // OUTPUT : a b c .. .. .. .. y z
        char i;
        for (i = 'a'; i <='z' ; i++) {
            System.out.print(i+" ");
        }
    }
}
